class Album {
  final String id;
  final String name;
  final String releaseDate;
  final String imageUrl;
  final String artistId;
  final String artistName;

  Album({
    required this.id,
    required this.name,
    required this.releaseDate,
    required this.imageUrl,
    required this.artistId,
    required this.artistName,
  });
}
